        private static int CountTrees(string[] grid, int colOffset, int rowOffset)
        {
            var rowLength = grid[0].Length;
            var row = 0;
            var col = 0;
            var trees = 0;

            while (row < grid.Length - 1)
            {
                col = (col + colOffset) % rowLength;
                row += rowOffset;

                if (grid[row][col] == '#')
                    trees++;
            }

            return trees;
        }
